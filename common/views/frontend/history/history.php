                            <h1>История на Перущица</h1>
                            <hr>
                                                <img src="admin/images/people/stenopis_0.jpg" class="img-polaroid" style="margin:5px 0px 15px;" alt="">
                            <p><strong>„Перущице бледна, гнездо на герои, слава!  Вечна слава на чедата твои, на твойта пепел и на твоя гроб...“</strong>
                                Тези думи на поета завинаги определиха почетното място на Перущица в българската история. Градът се превърна в паметник
                                на безсмъртието, пред който се покланят признателните българи  и изумените чужденци.  В този град през април 1876 година
                                се чу най-мощният глас за свобода, който Европа беше чувала – мъже избиха жените и децата си, а после и себе си, за да не
                                бъдат роби. Този миг от Априлското въстание е изумителен по своята сила. В църквата „Св. Архангел Михаил“ умират 347 свободни
                                българи, притиснати от башибозука на Адил Ага. След седемдневна битка Перущица е по-свободна отвсякога. Европа и светът са смълчани
                                от подвига на Кочо Честеменски, Спас Гинев, Иван Хаджитлиев.</p>

                            <p>По повод 20-годишнината на Априлското въстание, през 1896 година се създава перущинският комитет „27 априлий“, който има задачата да
                                „въздигне паметник на мъченически загиналите за свободата на България през 1876 година перущенци”, а и да пази тяхното дело.
                                След Априлското въстание костите на героите са погребани  извън пределите на някогашна Перущица.
                                С решение на комитета „27 априлий“ и общинската управа костите на априлци са върнати в църквата „Св. Архангел Михаил“.
                                Градът е освободен от османско владичество на 3 януари 1878 година. Стогодишнината от Освобождението е отбелязана
                                с „Паметник на трите поколения“, издигнат на хълма „Власевица“.</p>

                            <p>Районът на Перущица е богат на археологически находки. Един от най-старите паметници с голяма историческа стойност е Червената църква,
                                наричана още „Света Богородица“. Храмът е изграден през ранното средновековие - V–VI век от новата ера, и е един от
                                най-значимите и оригинални раннохристиянски базилики на Балканите. Той е с внушителни размери за времето си.
                                В Червената църква умело са съчетани античният гръцки с новия византийски стил и някои форми на римската мавзолейна архитектура,
                                спазвайки изискванията на християнския култ.</p>

                            <p>Перущица се намира в северното подножие на върховръшкия дял на Родопите.
                                Името си носи от Перистица – укрепено място от времето на Средновековието.
                                Крепостта е  била част от отбранителната верига на Второто българско царство.
                                Смята се, че сегашният град води началото си от древния град Драговец,
                                разположен на 2-3 километра северно от днешните му граници и разрушен oт ордите на турския пълководец Лала Шахин през 1364 година.
                                Опитите на турците да помохамеданчат местното население не успели. През XV век тук се заселили бежанци – българи от най-западните предели на Балканския полуостров (днешни Албания и Епир).
                                По-късно селището било ограбено и опожарено отново през 1794 година от кърджалиите на Мехмед Синап.</p>

                            <p>В Перущица има ясни следи на римската и тракийската цивилизации. Векове преди идването на римляните, свое селище около карстовия извор в някогашния квартал Пастуша са създали траките.
                                В този район са открити 29 надгробни  могили, сред които най-известни са Духова и Банова.
                                В основите на Духова могила при разкопки са намерени тракийски златни колесници.
                                Тези находки показват, че цивилизацията по тези земи е свързана с  най-стария жив град в Европа – Пловдив.
                                Находките от Духова  могила  са експонирани днес в Историческия музей в Перущица -  тракийски бойни колесници, мраморна глава на мъж, статуи на антични божества, оброчни плочки, както и монети,
                                сечени във Филипопол.</p>

                            <p>По време на Възраждането в Перущица са живели и работили видни просветители и дейци на националноосвободителното движение,
                                като Петър Бонев, Спас Гинов, Кочо Честименски, Васил Соколски-Докторът. През 1869 г.
                                Васил Левски основал тук таен революционен комитет, чиято дейност била възобновена по-късно през 1876 година от Георги Бенковски.</p>

                            <p>Перущица съществува като самостоятелна община от 1998 година и е разположена върху площ от 48 720 декара.
                                В нейната територия единственото населено място е град Перущица. По данни от последното преброяване жителите на общината са 5058.
                                Намира се на 24 километра от Пловдив и разполага с добра пътна инфраструктура. Граничи с общините Кричим, Стамболийски и Родопи.</p>

                            <p>Комплексът „Исторически музей“ включва четири експозиционни зали, които проследяват развитието на селището от неолита до съвременния му облик.
                                Допълнение към тях е  църквата „Св. Архангел Михаил“ – пантеон на Апилската епопея.
                                Етнографската експозиция на Перущица  представя оръдия  труда, облекло, съдове и накити на местното население.
                                Дановото училище е първото класно школо, където Христо Данов прилага взаимоучителния метод.
                                Археологическите обекти са представени  от раннохристиянската базилика Червената църква, Римския път за Беломорието, развалините на манастирския комплекс  „Св. Тодор Тирон и Стратилат“.</p>

                            <p>Основно място в живота и поминъка на Перущица заемат лозарството и производството на вино.
                                Още през периода на Втората българска държава всяко семейство работи лозя, и особено манастирски лозя.
                                За гроздобера Константин Гълъбов пише: „Перущинец и в Китай да е, гледа да си дойде за гроздобер“.
                                За Трифон Зарезан пеели песента „В Перущица с вино карат воденица“:</p>

