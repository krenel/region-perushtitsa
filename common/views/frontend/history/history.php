                            <p>Голямата атракция за града и до днес е обичаят „Къпанки“, който е на Ивановден по стар стил.
                                Той е характерен само за Перущица и е единствен на  Балканите.
                                Предполага се, че обичаят е наследство от древните славянски празници, свързани с водата.
                                Зетьовете, които са  най-пресните младоженци, се къпят обредно под Чалъковия мост.
                                Значението на обичая е да има здраве, момъкът да се почувства мъж, да са плодовити булките.
